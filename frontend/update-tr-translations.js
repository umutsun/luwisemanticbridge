function updateKeys(obj, path = '') {
    for (const key in obj) {
        const currentPath = path ? `${path}.${key}` : key;

        if (typeof obj[key] === 'object' && obj[key] !== null) {
            updateKeys(obj[key], currentPath);
        } else if (typeof obj[key] === 'string') {
            // Check if we have a translation for this exact path
            if (TRANSLATIONS[currentPath]) {
                obj[key] = TRANSLATIONS[currentPath];
            }
            // Check if we have a translation for just the key (for root level keys)
            else if (TRANSLATIONS[key]) {
                obj[key] = TRANSLATIONS[key];
            }
        }
    }
}

